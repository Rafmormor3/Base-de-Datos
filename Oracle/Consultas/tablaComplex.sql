CREATE TABLE EMPLE 
(
	EMP_NO NUMBER(5),
	APELLIDO VARCHAR2(20),
	OFICIO VARCHAR2(20),
	DIR NUMBER(5),
	FECHA_ALT DATE,
	SALARIO NUMBER(8),
	COMISION NUMBER(8),
	DEPT_NO NUMBER(3),
	
	CONSTRAINT PK_EMPLE PRIMARY KEY (EMP_NO)
	
);

CREATE TABLE DEPART 
(
	DEPT_NO NUMBER(3),
	DNOMBRE VARCHAR2(20),
	LOC VARCHAR2(20),

	CONSTRAINT PK_DEPART PRIMARY KEY (DEPT_NO)	
);

CREATE TABLE HERRAMIENTAS
(
	DESCRIPCION VARCHAR2(20),
	ESTANTERIA NUMBER(1),
	UNIDADES NUMBER(2),
	CONSTRAINT PK_HERRAMIENTAS PRIMARY KEY (DESCRIPCION)
);

CREATE TABLE HOSPITALES
(
	COD_HOSPITAL NUMBER(3),
	NOMBRE VARCHAR2(30),
	DIRECCION VARCHAR2(50),
	NUM_PLAZAS NUMBER(3),
	
	CONSTRAINT PK_HOSPITALES PRIMARY KEY (COD_HOSPITAL)
);

CREATE TABLE MEDICOS
(
	COD_HOSPITAL NUMBER(2),
	DNI VARCHAR2(9),
	APELLIDOS VARCHAR2(30),
	ESPECIALIDAD VARCHAR2(20),
	
	CONSTRAINT PK_MEDICOS PRIMARY KEY(DNI)

);

CREATE TABLE PERSONAS
(
	DNI VARCHAR2(9),
	COD_HOSPITAL NUMBER(10),
	APELLIDOS VARCHAR2(30),
	FUNCION VARCHAR2(20),
	SALARIO NUMBER(10),
	CONSTRAINT PK_PER PRIMARY KEY (DNI),
	CONSTRAINT FK_PE FOREIGN KEY (COD_HOSPITAL) REFERENCES HOSPITALES(COD_HOSPITAL)
);

